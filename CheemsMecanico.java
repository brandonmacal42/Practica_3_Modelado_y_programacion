
public class CheemsMecanico {

   public void preparaComboUno(Nave com){
      com.agregarBlindaje();
      com.agregarArma();
      com.agregarCabina();
      com.agregarPropulsion();
   }   

   public void preparaComboDos(Nave com){
      com.agregarHamburguesa();
      com.agregarBebida();
      com.agregarPapas();
   }

   public void preparaComboTres(Nave com){
      com.agregarHamburguesa();
      com.agregarBebida();
      com.agregarPapas();
   }
}